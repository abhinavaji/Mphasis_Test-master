DROP TABLE IF EXISTS FINANCIAL_DTL;
DROP TABLE IF EXISTS SECTOR_DTL;
DROP TABLE IF EXISTS FACILITY_DTL;

  
CREATE TABLE SECTOR_DTL (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  SECTOR_NAME VARCHAR(250) NOT NULL
);

CREATE TABLE FACILITY_DTL (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  FACILITY_NAME VARCHAR(250) NOT NULL 
);

  
CREATE TABLE FINANCIAL_DTL (
  ID INT AUTO_INCREMENT  PRIMARY KEY,
  CUSTOMER_NAME VARCHAR(250) NOT NULL,
  EXPOSURE INT NOT NULL,
  CUSTOMER_TYPE VARCHAR(250) NOT NULL,
  SECTOR_ID INT NOT NULL,
  foreign key (SECTOR_ID) references SECTOR_DTL(ID),
  FACILITY_ID INT NOT NULL,
  foreign key (FACILITY_ID) references FACILITY_DTL(ID)
);


